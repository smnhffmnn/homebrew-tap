  depends_on arch: :arm64

  url "https://github.com/smnhffmnn/mux/releases/download/v0.6.5/mux_0.6.5_darwin_arm64.tar.gz"
  sha256 "331b4345e768479d8a3245385b2aa39ec0457e4fbfdf549d223e288edc30f899"

  def install
    bin.install "mux"
  end

  test do
    assert_match "mux", shell_output("#{bin}/mux --version", 0)
  end
end
