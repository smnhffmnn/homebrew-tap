  homepage "https://github.com/smnhffmnn/mux"
  version "0.10.0"
  license "MIT"

  depends_on :macos
  depends_on arch: :arm64

  url "https://github.com/smnhffmnn/mux/releases/download/v0.10.0/mux_0.10.0_darwin_arm64.tar.gz"
  sha256 "8ca04784aaac5baeea4668aef150bfeae1782bc50b811a338be48a26ba7cc28e"

  def install
    bin.install "mux"
  end

  test do
    assert_match "mux", shell_output("#{bin}/mux --version", 0)
  end
end
