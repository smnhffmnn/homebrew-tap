  version "0.13.1"
  license "MIT"

  depends_on :macos
  depends_on arch: :arm64

  url "https://github.com/smnhffmnn/mux/releases/download/v0.13.1/mux_0.13.1_darwin_arm64.tar.gz"
  sha256 "79a86c9a79233d97441f534c1e4701cedc39e32929879888f7309fee924671d9"

  def install
    bin.install "mux"
  end

  test do
    assert_match "mux", shell_output("#{bin}/mux --version", 0)
  end
end
