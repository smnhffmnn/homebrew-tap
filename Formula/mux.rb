  version "0.18.0"
  license "MIT"

  depends_on :macos
  depends_on arch: :arm64

  url "https://github.com/smnhffmnn/mux/releases/download/v0.18.0/mux_0.18.0_darwin_arm64.tar.gz"
  sha256 "c727231081e05bfdb9b8bda4e430d23af8664fd675876c8d529119b210a66ed7"

  def install
    bin.install "mux"
  end

  test do
    assert_match "mux", shell_output("#{bin}/mux --version", 0)
  end
end
